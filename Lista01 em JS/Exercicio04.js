const numero1 = parseInt(prompt("Informe o primeiro número:"));
const numero2 = parseInt(prompt("Informe o segundo número:"));
const numero3 = parseInt(prompt("Informe o terceiro número:"));
const numero4 = parseInt(prompt("Informe o quarto número:"));

const media = (numero1 + numero2 + numero3 + numero4) / 4


alert(`A média final é: ${media}`);